-- 1. CATEGORÍAS -------------------------------------------------
create table if not exists categories (
  id bigint generated by default as identity primary key,
  name text not null unique,
  slug text not null unique,
  created_at timestamptz default now()
);

-- 2. PRODUCTOS --------------------------------------------------
create table if not exists products (
  id bigint generated by default as identity primary key,
  name text not null,
  description text,
  price numeric(10,2) not null check (price >= 0),
  sale_price numeric(10,2) check (sale_price is null or sale_price >= 0),
  category_id bigint references categories(id) on delete set null,
  image_url text,
  images text[] not null default '{}',
  pet_type text not null default 'ambos' check (pet_type in ('perro','gato','ambos')),
  stock int not null default 0 check (stock >= 0),
  is_featured boolean default false,
  created_at timestamptz default now()
);

-- Por si la tabla products ya existía sin estas columnas:
alter table products add column if not exists sale_price numeric(10,2);
alter table products add column if not exists images text[] not null default '{}';
alter table products add column if not exists pet_type text not null default 'ambos';

-- 3. CLIENTES ---------------------------------------------------
create table if not exists customers (
  id bigint generated by default as identity primary key,
  dni text not null unique,
  first_name text not null,
  last_name text not null,
  phone text not null,
  address text,
  created_at timestamptz default now()
);

-- 4. CUPONES ---------------------------------------------------
create table if not exists coupons (
  id bigint generated by default as identity primary key,
  code text not null unique,
  discount_percent int not null check (discount_percent > 0 and discount_percent <= 100),
  max_uses int,
  uses_count int default 0,
  valid_from timestamptz default now(),
  valid_until timestamptz,
  active boolean default true,
  created_at timestamptz default now()
);

-- 5. PEDIDOS ----------------------------------------------------
create table if not exists orders (
  id bigint generated by default as identity primary key,
  customer_name text,
  customer_dni text,
  customer_phone text,
  customer_address text,
  items jsonb not null,
  total_amount numeric(10,2) not null,
  coupon_code text,
  coupon_discount numeric(10,2) default 0,
  status text default 'Pendiente' check (status in ('Pendiente','Entregado','Cancelado')),
  created_at timestamptz default now()
);

-- Por si orders existía sin cupones:
alter table orders add column if not exists coupon_code text;
alter table orders add column if not exists coupon_discount numeric(10,2) default 0;

-- Por si la tabla orders ya existía sin estas columnas:
alter table orders add column if not exists customer_name text;
alter table orders add column if not exists customer_dni text;
alter table orders add column if not exists customer_address text;

